  Widget build(BuildContext context) {
    // DateTime currentTime = DateTime.now();
    // String formattedHour;
    if(currentTime.hour - 11 <= 0){
      formattedHour = '${24-currentTime.hour  }시';
    }else{
      formattedHour = '${currentTime.hour -12  }시';
    }


    // String formattedTime;
    // if(currentTime.minute >= 30){
    //   formattedTime='30분';
    // }else{
    //   formattedTime='00분';
    // }
    if(currentTime.minute<10){
      formattedTime='0'+currentTime.minute.toString()+'분';
    }else{
      formattedTime=currentTime.minute.toString()+'분';
    }

    return AspectRatio(

      aspectRatio: 1,
      child: Stack(
        children: <Widget>[
          Padding(
            padding: const EdgeInsets.all(16),
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.center,
              children: <Widget>[
                const Text(
                  '요일별 혼잡도 통계',
                  style: TextStyle(
                    color: AppColors.contentColorRed,
                    fontSize: 24,
                    fontWeight: FontWeight.bold,
                  ),
                ),
                const SizedBox(
                  height: 4,
                ),
                Text(
                  '시간 기준 : $formattedHour $formattedTime',
                  style: TextStyle(
                    color: Color(0xFF757575),
                    fontSize: 18,
                    fontWeight: FontWeight.bold,
                  ),
                ),
                const SizedBox(
                  height: 38,
                ),
                Expanded(
                  child: Padding(
                    padding: const EdgeInsets.symmetric(horizontal: 8),
                    child: BarChart(
                      isPlaying ? randomData() : mainBarData(),
                      swapAnimationDuration: animDuration,
                    ),
                  ),
                ),
                const SizedBox(
                  height: 12,
                ),
              ],
            ),
          ),
          // Padding(
          //   padding: const EdgeInsets.all(8),
          //   child: Align(
          //     alignment: Alignment.topRight,
          //     child: IconButton(
          //       icon: Icon(
          //         isPlaying ? Icons.pause : Icons.play_arrow,
          //         color: AppColors.contentColorRed, //재생버튼별
          //       ),
          //       onPressed: () {
          //         setState(() {
